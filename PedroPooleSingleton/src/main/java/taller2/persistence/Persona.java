package taller2.persistence;

public final class Persona {
	private String			name;
	private int				age;
	
	/**No args constructor*/
	public Persona() {
		super();
	}
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	
}
